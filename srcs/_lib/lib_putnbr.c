void lib_putnbr(int n)
{
    char c;
    c = 0;
    if (n == -2147483648){
        write(1, "-2147483648", 11);
        return ;
    }
    if (n < 0){
        write(1, "-", 1);
        n = -n;
    }
    if (n >= 10)
        lib_putnbr(n / 10);
    c = (n % 10) + '0';
    write(1, &c, 1);
}
